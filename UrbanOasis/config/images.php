// Function to get a random image for a property type
function getRandomPropertyImage($propertyType) {
    global $unsplashImages;
    if (isset($unsplashImages[$propertyType])) {
        return $unsplashImages[$propertyType][array_rand($unsplashImages[$propertyType])];
    }
    // Fallback to house image if property type not found
    return $unsplashImages['house'][0];
}

// Function to get all images for a property type
function getPropertyImages($propertyType) {
    global $unsplashImages;
    return $unsplashImages[$propertyType] ?? $unsplashImages['house'];
}

// Function to get the correct image URL based on image type
function getImageUrl($imageUrl, $imageType) {
    if ($imageType === 'demo') {
        // For demo images, return the Unsplash URL directly
        return $imageUrl;
    } else {
        // For uploaded images, construct the local path
        if (strpos($imageUrl, 'uploads/') === 0) {
            return $imageUrl; // Already has the correct path
        } else {
            return 'uploads/' . $imageUrl; // Add uploads/ prefix if not present
        }
    }
}
